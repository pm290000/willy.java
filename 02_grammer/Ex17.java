class Ex17{
	public static void main(String[] args){

	//대입 연산자 : 특정 값이나 객테의 주소를 변수에 기억시킬 때 사용 (저장)
	// +=, -=, *=, /=, %=
	

	int su1 = 7;
	
	su1 += 4;  // su1 = su1 + 4;
	System.out.println(su1);

	su1 -= 4; // su1 = su1 - 4;
	System.out.println(su1);

	su1 *= 4; // su1 = su1 * 4;
	System.out.println(su1);

	su1 /= 4; // su1 = su1 / 4;
	System.out.println(su1);

	su1 %= 4; // su1 = su1 % 4;
	System.out.println(su1);


	}
}
